export const GetOnlyFood = () => {
  const only_restos: any[] = [];
  data.forEach((itemResto) => {
    itemResto.categories.forEach((itemCateg) => {
      itemCateg.plates.forEach((plate) => {
        only_restos.push({
          ...plate,
          categName: itemCateg.name,
          categLink: itemCateg.link,
          categImage: itemCateg.image,
          restoName: itemResto.image,
          restoImage: itemResto.link,
        });
      });
    });
  });
  return only_restos;
};

export const GetMyFoods = (my_plates_id: any[] = []) => {
  const all_plates = GetOnlyFood();
  const myPlates: any[] = [];

  all_plates.forEach((plate) => {
    let plate_id = plate.id;
    let exist = my_plates_id.indexOf(plate_id) !== -1;
    if (exist) {
      myPlates.push(plate);
    }
  });

  return myPlates;
};
